import SwiftUI

// Generous breathing room is part of the brand. Lean toward larger
// values when in doubt — the brand never crowds.

extension CGFloat {

    /// 4pt. Tiny gap, e.g., between an icon and its label.
    static let quellSpace1: CGFloat = 4

    /// 8pt. Small gap between related elements.
    static let quellSpace2: CGFloat = 8

    /// 12pt. Modest spacing.
    static let quellSpace3: CGFloat = 12

    /// 16pt. Standard padding and default gap.
    static let quellSpace4: CGFloat = 16

    /// 24pt. Comfortable section spacing.
    static let quellSpace5: CGFloat = 24

    /// 32pt. Clear separation between sections.
    static let quellSpace6: CGFloat = 32

    /// 48pt. Large breathing room between major regions.
    static let quellSpace7: CGFloat = 48

    /// 64pt. Hero spacing around presence moments.
    static let quellSpace8: CGFloat = 64
}
